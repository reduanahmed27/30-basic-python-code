#Write a program to enter two numbers and perform all arithmetic operations.

a = float(input("Enter a number: "))
b = float(input("Enter another number: "))
add = a+b
sub = a-b
mul = a*b
div = a/b
mod = a%b
print("Addition  = ",add)
print("Subtraction = ",sub)
print("Multiplication = ",mul)
print("Division = ",div)
print("Modulas = ",mod)